function quickStart() {
    // Get the callout element
    var callin = document.getElementById("quick-start");
    var callout = document.getElementById("main-dashboard");

    // Display
    callin.style.display = "block";
    callout.style.display = "none";
}

function hideImage() {
    // Get the callout element
    var callout = document.getElementById("quick-start");

    // Hide
    callout.style.display = "none";
}

